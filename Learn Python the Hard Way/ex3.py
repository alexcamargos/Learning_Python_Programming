print('Is it true tha 3 + 2 < 5 -7?')

# Mostra o resultado da comparação de boleana de 5 e -2, ou seja False.
print(3 + 2 < 5 - 7)

# Mostra o resultado da operação matemática 3 mais 2, resultado 5.
print('What is 3 + 2?', 3 + 2)
# Mostra o resultado da operação matemática 5 menos 7, resultado -2.
print('What is 5 - 7?', 5 - 7)

