"""
The following snippet can be used to convert a string to title case.
This is done using the title() method of the string class.
"""


my_name = 'my name is alexsander lopes camargos.'

# using the title() function of string class
new_name = my_name.title()

print(f'String base: {my_name}')
print(f'New String: {new_name}')
